import React, { Component } from 'react';
import styled from "styled-components";
import {RightLayoutA} from "./HowToUse/RightLayoutA";
import {LeftLayoutA} from "./HowToUse/LeftLayoutA";
import {ShareLowerLayout} from "./HowToUse/ShareLowerLayout";
import {RightLayoutB} from "./HowToUse/RightLayoutB";
import {RightLayoutC} from "./HowToUse/RightLayoutC";
import {ThemeColor} from "../../common/Css2.js";
import {EvvaLeft} from "./HowToUse/EvvaLeft";
import {EvvaRight} from "./HowToUse/EvvaRight";
import {EvvaLower} from "./HowToUse/EvvaLower";

const OutterWrapper=styled.div`
display:flex;
justify-content:center;
align-items:center;
width:100%;
flex-direction:column;
background-color:${(props)=>props.color};
`;

const Whiteline=styled.div`
width:95%;
height:2px;
background-color:white;
`

const Row=styled.div`
display:flex;
flex-direction:row;
width:100%;
`;

const Cell=styled.div`
display:flex;
flex-direction:column;
width:100%;
border:0px solid red;
`;

const TitleDiv=styled.div`
width:100%;
background-color:rgb(253,169,141);
text-align:center;
`;
const Title=styled.label`
font-size:21pt;
font-weight:bold;
`;

export const HowToUseView=({
    productID,color,RLayout,LLayout,
    gender='g'
})=>{
    var LeftLayoutType='A';
    var backgroundcolor =ThemeColor[gender].how2Use; 

    //special handling of EVVA
    if(productID=='EVVA'){
        return(
            <OutterWrapper color={backgroundcolor}>
            <div id="how2use"></div>

            <Row>
            <Cell>
            <EvvaLeft
                productID={productID}
                color={color}
                />   
            </Cell>
            <Cell>
            <EvvaRight
                productID={productID}
                color={color}
                />   
            </Cell>
            </Row>
            <Row>
            <EvvaLower
                            productID={productID}
                            color={color}
            />
            </Row>
            </OutterWrapper>
        );
    }


    return(
        <OutterWrapper
            color={backgroundcolor}
        >
             <div id="how2use"></div>
             <Row>
                
                <Cell>
                <TitleDiv>
                    <Title>
                     </Title>
                </TitleDiv>    

                {LLayout == 'A' &&
                <LeftLayoutA
                productID={productID}
                color={color}
                />       
                }
                </Cell>
                <Cell>    
                <TitleDiv>

                    <Title>
                     </Title>
                </TitleDiv>    

                {RLayout == 'A' &&
                <RightLayoutA
                productID={productID}
                color={color}
                />
                 }

                {RLayout == 'B' &&
                <RightLayoutB
                productID={productID}
                color={color}
                />
                 }
                {RLayout == 'C' &&
                <RightLayoutC
                productID={productID}
                color={color}
                />
                 }

                </Cell>
                
            </Row>
            <Whiteline/>
            <Cell>    
            <ShareLowerLayout
                productID={productID}
                color={color}
            />
            </Cell>
        </OutterWrapper>
    );

}